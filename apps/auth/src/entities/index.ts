/**
 * Единая точка входа для всех сущностей приложения.
 *
 * Экспортирует основные бизнес-сущности, типы и константы,
 * доступные по всему приложению через `@entities`.
 *
 * Используется для:
 * - Централизованного импорта
 * - Упрощения путей (например, `@entities/index`)
 *
 * @example
 * import { User, IUserDto } from '@entities';
 */
export {
  /**
   * DTO пользователя — данные, полученные от сервера.
   *
   * Содержит всю информацию о пользователе: id, email, имя, роль и т.д.
   * Используется для создания экземпляра класса User.
   */
  IUserDto,

  /**
   * Интерфейс для обновления данных пользователя.
   *
   * Определяет, какие поля можно изменить (частичное обновление).
   * Все поля необязательные.
   */
  IUserUpdateData,

  /**
   * Бизнес-сущность пользователя.
   *
   * Инкапсулирует данные и поведение: проверку прав, статус активности,
   * формирование имени и другие методы.
   */
  User,

  /**
   * Константы, связанные с пользователем.
   *
   * Включает пороги времени (например, для определения "онлайн"),
   * значения по умолчанию и другие фиксированные значения.
   */
  USER_CONSTANTS,

  /**
   * Объект с ключами ролей пользователя.
   *
   * Предоставляет строковые значения ролей в виде констант:
   * - USER: 'user'
   * - MODERATOR: 'moderator'
   * - ADMIN: 'admin'
   *
   * Полезно для сравнения и предотвращения опечаток.
   */
  USER_ROLES,

  /**
   * Массив всех возможных ролей.
   *
   * Используется для валидации входящих данных:
   * ```ts
   * if (USER_ROLES_LIST.includes(inputRole)) { ... }
   * ```
   */
  USER_ROLES_LIST,

  /**
   * Тип роли пользователя.
   *
   * Допустимые значения: 'user' | 'moderator' | 'admin'.
   * Определяет уровень доступа в системе.
   */
  UserRole
} from '@entities/user';
