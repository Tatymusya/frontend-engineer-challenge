/**
 * Тип роли пользователя в системе.
 *
 * Определяет уровень доступа и разрешённые действия.
 * - 'user' — обычный пользователь
 * - 'moderator' — модератор (дополнительные права)
 * - 'admin' — администратор (полный доступ)
 */
export type UserRole = 'user' | 'admin' | 'moderator';

/**
 * Список всех возможных ролей.
 *
 * Используется для валидации, отображения в UI, проверки прав.
 */
export const USER_ROLES = {
  USER: 'user',
  MODERATOR: 'moderator',
  ADMIN: 'admin',
} as const;

/**
 * Массив всех значений ролей.
 *
 * Полезно для проверки входящих данных:
 * ```ts
 * if (USER_ROLES_LIST.includes(inputRole)) { ... }
 * ```
 */
export const USER_ROLES_LIST = Object.values(USER_ROLES) as readonly UserRole[];

/**
 * DTO пользователя — данные, полученные от сервера.
 *
 * Содержит всю основную информацию о пользователе,
 * включая идентификатор, контактные данные, роль и статус активности.
 * Используется для создания экземпляра класса User.
 */
export interface IUserDto {
  /**
   * Уникальный идентификатор пользователя.
   *
   * Генерируется на сервере. Используется для ссылок, запросов и идентификации.
   *
   * @example "usr_123abc456def"
   */
  id: string;

  /**
   * Электронная почта пользователя.
   *
   * Уникальное значение, используется для входа и восстановления доступа.
   * Проверяется на валидность при регистрации.
   *
   * @example "user@example.com"
   */
  email: string;

  /**
   * Имя пользователя.
   *
   * Может отсутствовать (null). Если указано — отображается в интерфейсе.
   *
   * @example "Иван"
   * @example null
   */
  firstName?: string | null;

  /**
   * Фамилия пользователя.
   *
   * Может отсутствовать (null). Используется вместе с именем для формирования полного имени.
   *
   * @example "Иванов"
   * @example null
   */
  lastName?: string | null;

  /**
   * Номер телефона в международном формате.
   *
   * Опционально. Может использоваться для двухфакторной аутентификации или связи.
   *
   * @example "+79991234567"
   * @example undefined
   */
  phone?: string;

  /**
   * Роль пользователя в системе.
   *
   * Определяет уровень доступа и доступные действия.
   */
  role: UserRole;

  /**
   * Дата и время создания аккаунта.
   *
   * Представлена в формате ISO 8601 как строка.
   * Перед использованием следует преобразовать в объект Date.
   *
   * @example "2024-01-01T10:00:00.000Z"
   */
  createdAt: string; // ISO

  /**
   * Дата и время последней активности пользователя.
   *
   * Отражает момент последнего взаимодействия с системой.
   * Может быть null, если активность ещё не фиксировалась.
   *
   * @example "2024-01-02T14:30:00.000Z"
   * @example null
   */
  lastActiveAt?: string | null;

  /**
   * Флаг подтверждения email.
   *
   * Показывает, прошёл ли пользователь верификацию электронной почты.
   * Некоторые функции могут быть недоступны до подтверждения.
   */
  isEmailVerified: boolean;

  /**
   * Флаг авторизованного в системе пользователя.
   *
   */
  isAuthenticated: boolean,
}

/**
 * Интерфейс данных для обновления профиля пользователя.
 *
 * Используется при отправке запроса на изменение информации о пользователе.
 * Все поля необязательные, так как клиент может обновлять только часть данных.
 */
export interface IUserUpdateData {
  /**
   * Имя пользователя.
   *
   * Допустимые значения: строка, не более 50 символов.
   * Может быть пустой строкой или null — в этом случае имя будет удалено.
   *
   * @example "Иван"
   * @example "Анна"
   */
  firstName?: string | null;

  /**
   * Фамилия пользователя.
   *
   * Допустимые значения: строка, не более 50 символов.
   * Может быть пустой строкой или null — в этом случае фамилия будет удалена.
   *
   * @example "Иванов"
   * @example "Петрова"
   */
  lastName?: string | null;

  /**
   * Номер телефона пользователя в международном формате.
   *
   * Формат: строка, начинающаяся с '+' и кода страны.
   * Проверяется на валидность на сервере.
   *
   * @example "+79991234567"
   * @example "+380501234567"
   */
  phone?: string | null;

  /**
   * Флаг подтверждения email.
   *
   * Показывает, прошёл ли пользователь верификацию электронной почты.
   * Некоторые функции могут быть недоступны до подтверждения.
   */
  isEmailVerified?: boolean,

  /**
   * Флаг авторизованного в системе пользователя.
   *
   */
  isAuthenticated?: boolean,
}

/**
 * Данные для смены пароля.
 */
export interface IChangePasswordData {
  /**
   * Текущий пароль пользователя.
   */
  currentPassword: string;

  /**
   * Новый пароль.
   * Требования: минимум 8 символов, есть цифра и буква.
   */
  newPassword: string;

  /**
   * Подтверждение нового пароля.
   */
  confirmPassword: string;
}